

-- What is a database?
-- A database is a collection of data that is stored in a computer system.
--Databases allow their users to enter, access, and analyze their data quickly and easily. 
--They are used in a variety of industries, including banking, manufacturing, and publishing. Databases are also used to store information more specifically.
--For example, banks store information about their customers and accounts.
--Libraries store information about their books and borrowers. And online stores store information about their products and customers.


-- What is a schema?
-- A schema is a collection of database objects (as far as this class is concerned, tables) associated with one particular database username.

-- What is a table?
-- A table is a collection of related data held in a table format within a database.
-- It consists of columns and rows. In relational databases, and flat file databases, a table is a set of data elements (values) 
--using a model of vertical columns (identifiable by name) and horizontal rows, the cell being the unit where a row and column intersect.

